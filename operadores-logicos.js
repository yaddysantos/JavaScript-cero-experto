//operadores logicos
let a = true;
let b = false;

console.log(a);
console.log(b);

//Operador logico (and)

console.log(`${a} && ${b} -> ${a && b}`); // true && false -> false

//Operador logico (or - ||)
console.log(`${a} || ${b} -> ${a || b}`); // true || false -> false

//Operador ! (nor or NO)
console.log(`${a} -> ${!a}`);